#Створення змінної для збереження цілочисельного значення, користувач має ввести будь-яке ціле число
First_num = int(input("Введіть ціле число: "))

#Розрахунок остачі від ділення на 2
Rem_from_div = First_num % 2

#Розрахунок формули для знаходження наступного парного числа
Next_pair_num = First_num + 2 - Rem_from_div

#Виведення резулятату задачі
print(f"{Next_pair_num} наступне парне число")